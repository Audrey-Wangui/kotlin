fun main(){
    // Looping from a high number to a lower number by use of for loop

    for (x in 10 downTo 1){
        println(x)
    }

    print("=========")

    // Create a program  that fids the odd number from 20 to 10
    for (d in 20 downTo 10){
        if(d % 2 != 0){
            println(d)
        }
    }
}